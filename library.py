import string

# Obter letras maiúsculas
uppercase_letters = string.ascii_uppercase
print("Letras maiúsculas:", uppercase_letters)

#Obter O comprimento:
comprimento = len(uppercase_letters)
print(comprimento)

# Obter letras minúsculas
lowercase_letters = string.ascii_lowercase
print("Letras minúsculas:", lowercase_letters)

# Obter letras maiúsculas e minúsculas combinadas
letters = string.ascii_letters
print("Letras:", letters)

# Obter dígitos numéricos
digits = string.digits
print("Dígitos numéricos:", digits)

# Obter pontuações
punctuation = string.punctuation
print("Pontuações:", punctuation)
